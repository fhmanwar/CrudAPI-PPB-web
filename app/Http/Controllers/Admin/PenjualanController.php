    {
        $data = DB::table('penjualans')
                ->join('users','users.id', '=', 'penjualans.id_user')
                ->join('anggreks','anggreks.id', '=', 'penjualans.id_anggrek')
                ->join('orders','orders.id', '=', 'penjualans.id_order')
                ->where('users.akses','=','user')
                ->get();


        return $data;
    }

